// CW6: Pierwsze zmienne
// ======================
// PRAKTYKA: Zmienne przechowują wartości
fn praktyka_cw6() {
    let x = 5;
    let y = 10;
    println!("x = {}", x);
    println!("y = {}", y);
    println!("x + y = {}", x + y);
    println!("x * y = {}", x * y);
}

// WYJAŚNIENIE:
// let = utwórz zmienną
// x, y = nazwy zmiennych
// = 5, = 10 = przypisz wartości
//
// ZADANIE:
// Utwórz 3-4 zmienne (liczby) i wydrukuj ich wyniki operacji.

