// PRAKTYKA: Różne obliczenia
fn praktyka_cw5() {
    println!("10 + 5 = {}", 10 + 5);
    println!("20 - 3 = {}", 20 - 3);
    println!("6 * 7 = {}", 6 * 7);
    println!("20 / 4 = {}", 20 / 4);
    println!("10 % 3 = {}", 10 % 3);  // % to modulo (reszta)
}

